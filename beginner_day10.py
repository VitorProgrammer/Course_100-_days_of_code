def add(n1, n2):
    return n1 + n2

def sub(n1, n2):
    return n1 - n2

def mult(n1, n2):
    return n1 * n2

def div(n1, n2):
    return n1 / n2

operations = {
    "+": add,
    "-": sub,
    "*": mult,
    "/": div
}

def denovo():
    print(logo)
    num1 = float(input("What's the first number? "))
    again = True
    while again:
        for op in operations:
            print(op)
        choose1 = input("What operation do you choose? ")
        num2 = float(input("What's the next number? "))
        calculation = operations[choose1]
        answer = calculation(num1, num2)

        print(f"{num1} {choose1} {num2} = {answer}")

        yes = input(f"Do you want to continue operations with {answer}? 'y' or 'n': ")
        if yes == "y":
            num1 = answer
        else:
            denovo()
denovo()


    